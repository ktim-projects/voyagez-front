const authStore = useAuthStore();

const email = ref('');
const password = ref('');
const rememberMe = ref(false);
const loading = ref(false);

const handleLogin = async () => {
  loading.value = true;
  try {
    const success = await authStore.login(email.value, password.value);
    if (success) {
      router.push('/');
    }
  } catch (error) {
    console.error('Login failed:', error);
  } finally {
    loading.value = false;
  }
};
